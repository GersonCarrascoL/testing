package pe.edu.unmsm.quipucamayoc.model;

public class BienCategoriaModel {	
	private Integer idBienCata;
	private Integer udIdCategoria;
	private String descCategoriaBien;
	private String filterCategoriaBien;
	private Integer status;
		
	public Integer getIdBienCata() {
		return idBienCata;
	}
	public void setIdBienCata(Integer idBienCata) {
		this.idBienCata = idBienCata;
	}
	public Integer getUdIdCategoria() {
		return udIdCategoria;
	}
	public void setUdIdCategoria(Integer udIdCategoria) {
		this.udIdCategoria = udIdCategoria;
	}
	public String getDescCategoriaBien() {
		return descCategoriaBien;
	}
	public void setDescCategoriaBien(String descCategoriaBien) {
		this.descCategoriaBien = descCategoriaBien;
	}
	public String getFilterCategoriaBien() {
		return filterCategoriaBien;
	}
	public void setFilterCategoriaBien(String filterCategoriaBien) {
		this.filterCategoriaBien = filterCategoriaBien;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	

}
