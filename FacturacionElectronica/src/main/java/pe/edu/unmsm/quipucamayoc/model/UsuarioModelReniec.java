package pe.edu.unmsm.quipucamayoc.model;

public class UsuarioModelReniec {
	private String nroDni;
	private String nomComp;
	private String cui;
	private String carVer;
	private int estServ;
	
	public String getNroDni() {
		return nroDni;
	}
	public void setNroDni(String nroDni) {
		this.nroDni = nroDni;
	}
	public String getNomComp() {
		return nomComp;
	}
	public void setNomComp(String nomComp) {
		this.nomComp = nomComp;
	}
	public String getCui() {
		return cui;
	}
	public void setCui(String cui) {
		this.cui = cui;
	}
	public String getCarVer() {
		return carVer;
	}
	public void setCarVer(String carVer) {
		this.carVer = carVer;
	}
	public int getEstServ() {
		return estServ;
	}
	public void setEstServ(int estServ) {
		this.estServ = estServ;
	}
	
	
}
