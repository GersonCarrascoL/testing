package pe.edu.unmsm.quipucamayoc.model;

public class PerfilModel {
	private String descripcionPerfil;
	private Integer estadoPefil;
	private Integer codigoPerfil;
	private Integer codigoModulo;
	private String descripcionGeneral;
	private String tipoPerfil;
	public String getDescripcionPerfil() {
		return descripcionPerfil;
	}
	public void setDescripcionPerfil(String descripcionPerfil) {
		this.descripcionPerfil = descripcionPerfil;
	}
	public Integer getEstadoPefil() {
		return estadoPefil;
	}
	public void setEstadoPefil(Integer estadoPefil) {
		this.estadoPefil = estadoPefil;
	}
	public Integer getCodigoPerfil() {
		return codigoPerfil;
	}
	public void setCodigoPerfil(Integer codigoPerfil) {
		this.codigoPerfil = codigoPerfil;
	}
	public Integer getCodigoModulo() {
		return codigoModulo;
	}
	public void setCodigoModulo(Integer codigoModulo) {
		this.codigoModulo = codigoModulo;
	}
	public String getDescripcionGeneral() {
		return descripcionGeneral;
	}
	public void setDescripcionGeneral(String descripcionGeneral) {
		this.descripcionGeneral = descripcionGeneral;
	}
	public String getTipoPerfil() {
		return tipoPerfil;
	}
	public void setTipoPerfil(String tipoPerfil) {
		this.tipoPerfil = tipoPerfil;
	}
	
	
}
