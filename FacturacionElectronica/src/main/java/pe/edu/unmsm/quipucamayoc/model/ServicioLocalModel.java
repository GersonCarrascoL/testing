package pe.edu.unmsm.quipucamayoc.model;

public class ServicioLocalModel {
	private Integer idMaquinaLocal;
	private Integer udIdAdministrativa;
	private String descripcionLocal;
	private Integer idMaquinaRegistradora;
	private Integer status;
	
	public Integer getIdMaquinaLocal() {
		return idMaquinaLocal;
	}
	public void setIdMaquinaLocal(Integer idMaquinaLocal) {
		this.idMaquinaLocal = idMaquinaLocal;
	}
	public Integer getUdIdAdministrativa() {
		return udIdAdministrativa;
	}
	public void setUdIdAdministrativa(Integer udIdAdministrativa) {
		this.udIdAdministrativa = udIdAdministrativa;
	}
	public String getDescripcionLocal() {
		return descripcionLocal;
	}
	public void setDescripcionLocal(String descripcionLocal) {
		this.descripcionLocal = descripcionLocal;
	}
	public Integer getIdMaquinaRegistradora() {
		return idMaquinaRegistradora;
	}
	public void setIdMaquinaRegistradora(Integer idMaquinaRegistradora) {
		this.idMaquinaRegistradora = idMaquinaRegistradora;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	
}
