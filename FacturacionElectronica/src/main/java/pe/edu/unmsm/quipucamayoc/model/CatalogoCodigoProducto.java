package pe.edu.unmsm.quipucamayoc.model;

public class CatalogoCodigoProducto {
	
	private String codigo;
	private String nombre;
	private String partidaReferencial;
	
	public String getCodigo() {
		return codigo;
	}
	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getPartidaReferencial() {
		return partidaReferencial;
	}
	public void setPartidaReferencial(String partidaReferencial) {
		this.partidaReferencial = partidaReferencial;
	}
	

}
