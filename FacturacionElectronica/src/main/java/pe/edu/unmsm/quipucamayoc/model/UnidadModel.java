
package pe.edu.unmsm.quipucamayoc.model;

public class UnidadModel {

	private Integer udId;
	private Integer udPad;
	private Integer nivunicod;
	private Integer unidepcaldep;
	private String udCod;
	private String udDsc;
	
	public Integer getUdId() {
		return udId;
	}
	public void setUdId(Integer udId) {
		this.udId = udId;
	}
	public Integer getUdPad() {
		return udPad;
	}
	public void setUdPad(Integer udPad) {
		this.udPad = udPad;
	}
	public Integer getNivunicod() {
		return nivunicod;
	}
	public void setNivunicod(Integer nivunicod) {
		this.nivunicod = nivunicod;
	}
	public Integer getUnidepcaldep() {
		return unidepcaldep;
	}
	public void setUnidepcaldep(Integer unidepcaldep) {
		this.unidepcaldep = unidepcaldep;
	}
	public String getUdCod() {
		return udCod;
	}
	public void setUdCod(String udCod) {
		this.udCod = udCod;
	}
	public String getUdDsc() {
		return udDsc;
	}
	public void setUdDsc(String udDsc) {
		this.udDsc = udDsc;
	}

}