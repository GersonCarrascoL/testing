package pe.edu.unmsm.quipucamayoc.model;

public class DetraccionSunatModel {
	
	private String fecha;
	private Double importeMinimoSoles;
	private Double porcentaje;
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	public Double getPorcentaje() {
		return porcentaje;
	}
	public void setPorcentaje(Double porcentaje) {
		this.porcentaje = porcentaje;
	}
	public Double getImporteMinimoSoles() {
		return importeMinimoSoles;
	}
	public void setImporteMinimoSoles(Double importeMinimoSoles) {
		this.importeMinimoSoles = importeMinimoSoles;
	}
	
	
	

}
