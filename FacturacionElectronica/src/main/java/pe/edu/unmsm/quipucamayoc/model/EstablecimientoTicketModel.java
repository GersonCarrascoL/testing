package pe.edu.unmsm.quipucamayoc.model;

public class EstablecimientoTicketModel {
	
	private Integer udId;
	private String	codigoEstablecimiento;
	private String 	unidad;
	private String	ultimoTicket;
	private Integer	estado;
	private String	direccion;
	private String 	telefono;
	private String	ticketera;
	public Integer getUdId() {
		return udId;
	}
	public void setUdId(Integer udId) {
		this.udId = udId;
	}
	public String getCodigoEstablecimiento() {
		return codigoEstablecimiento;
	}
	public void setCodigoEstablecimiento(String codigoEstablecimiento) {
		this.codigoEstablecimiento = codigoEstablecimiento;
	}
	public String getUltimoTicket() {
		return ultimoTicket;
	}
	public void setUltimoTicket(String ultimoTicket) {
		this.ultimoTicket = ultimoTicket;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getTicketera() {
		return ticketera;
	}
	public void setTicketera(String ticketera) {
		this.ticketera = ticketera;
	}
	public String getUnidad() {
		return unidad;
	}
	public void setUnidad(String unidad) {
		this.unidad = unidad;
	}
	
	
}
