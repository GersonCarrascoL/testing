package pe.edu.unmsm.quipucamayoc.model;

public class TipoResolucionModel {
	
	private String codTipoResol;
	private String nameTipoResol;
	private Integer nivel;
	
	public String getCodTipoResol() {
		return codTipoResol;
	}
	public void setCodTipoResol(String codTipoResol) {
		this.codTipoResol = codTipoResol;
	}
	public String getNameTipoResol() {
		return nameTipoResol;
	}
	public void setNameTipoResol(String nameTipoResol) {
		this.nameTipoResol = nameTipoResol;
	}
	public Integer getNivel() {
		return nivel;
	}
	public void setNivel(Integer nivel) {
		this.nivel = nivel;
	}
	
	

}
