package pe.edu.unmsm.quipucamayoc.model;

public class TipoCambioModel {

	private String fechaTc;
	private Double factorTccompra;
	private Double factorTcventa;

	public String getFechaTc() {
		return fechaTc;
	}

	public void setFechaTc(String fechaTc) {
		this.fechaTc = fechaTc;
	}

	public Double getFactorTccompra() {
		return factorTccompra;
	}

	public void setFactorTccompra(Double factorTccompra) {
		this.factorTccompra = factorTccompra;
	}

	public Double getFactorTcventa() {
		return factorTcventa;
	}

	public void setFactorTcventa(Double factorTcventa) {
		this.factorTcventa = factorTcventa;
	}

}
