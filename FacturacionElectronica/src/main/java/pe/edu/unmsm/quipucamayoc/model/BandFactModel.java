package pe.edu.unmsm.quipucamayoc.model;

public class BandFactModel {
	private String ind_situ;
	private String num_ruc;
	private String tip_doc;
	private String num_doc;
	public String getInd_situ() {
		return ind_situ;
	}
	public void setInd_situ(String ind_situ) {
		this.ind_situ = ind_situ;
	}
	public String getNum_ruc() {
		return num_ruc;
	}
	public void setNum_ruc(String num_ruc) {
		this.num_ruc = num_ruc;
	}
	public String getTip_doc() {
		return tip_doc;
	}
	public void setTip_doc(String tip_doc) {
		this.tip_doc = tip_doc;
	}
	public String getNum_doc() {
		return num_doc;
	}
	public void setNum_doc(String num_doc) {
		this.num_doc = num_doc;
	}

	
}
